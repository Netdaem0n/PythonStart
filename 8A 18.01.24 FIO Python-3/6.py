# x - произвольное число часы (0 - 24)

x = int(input())

if (x >= 5) and (x <= 12):
    print('Время суток =', 'Утро')
elif (x >= 13) and (x <= 17):
    print('Время суток =', 'День')
elif (x >= 18) and (x <= 22):
    print('Время суток =', 'Вечер')
elif ((x >= 0) and (x <= 4)) or ((x >= 23) and (x <= 24)):
    print('Время суток =', 'Ночь')
